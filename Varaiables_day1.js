//Variables are containers to store data values  
//we assigning the  variables values we can use some keywords ex:- {let,var,const}
// VAR:-The var keyword is used to declare a variable. It has a function-scoped or globally-scoped behavior
var a= "hello world"//string
var b=10; //initialization and declaration
var c=30;
var d=20;
var e=b+c+d
x=10; //re-declaration
y=20;
z=x+y
console.log(x);
console.log(y);
console.log(z);
console.log(a);
console.log(a,b,c,d);
console.log(b);
console.log(c);
console.log(d);
console.log(e);

//let:-The let keyword is a block-scoped variables. It’s commonly used for variables that may change their value.
let f="Karimulla"
let g="Shaik"
let h=25;  
let i=35;
let j="32"
console.log(f);
console.log(g);
console.log(h);
console.log(i);
console.log(i+h);
console.log(j);
// const:-The const keyword declares variables that cannot be reassigned. It’s block-scoped as well.
 const pi=3.14
 const kj=400;
 const kl="67"
 console.log(pi); //can not change values for constant
 console.log(typeof(3.14));

const ab="hey"
console.log(ab);

console.log(kj);
console.log(kl);

const ca="Starc"
console.log(ca);

console.log(ab+ca);


