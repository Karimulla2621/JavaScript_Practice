//Operators in javaScript
// operators is used to perform mathematical operations 

//The Assignment Operator[=] assigns values

let a=10; // assign the value to 'a to 10' 
let b=20; // assign the value to 'b to 20'
let c=a+b;
console.log(c) // assign the values to 'c=a+b'

//The Addition Operator[+] adds values
let d=30;
let e=70;
let f=d+e
console.log(f);

//The subtraction operator (-) subtracts numbers.
let v=100;
let u=250;
let w=v-u;
console.log(w);

//The division operator (/) divides numbers.
let p=300;
let q=400;
let r=p/q;
console.log(r);


//The Multiplication Operator [*] multiplies values
let g=20;
let h=30;
let i=g*h;
console.log(i);

//	Remainder The modulus operator (%) returns the division remainder.
let m=20;
let n=40;
let l=m%n;
console.log(l);
//The increment operator (++) increments numbers.
let s=5;
s++
let ko=s
console.log(ko);

//The decrement operator (--) decrements numbers.
let t=10;
t--
let mk=t;
console.log(mk);

//The exponentiation operator (**) raises the first operand to the power of the second operand.
let uk=5;
let bv= uk ** 3;
console.log(bv);

//Arithmetic Operators are used to perform arithmetic on numbers:
let z=20;
let k=(20+50)*z
console.log(k);
